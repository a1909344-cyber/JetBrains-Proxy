export type TranslationKey = keyof typeof translations.zh;

type LangContextType = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TranslationKey, vars?: Record<string, string>) => string;
};

const LangContext = createContext<LangContextType>({
  lang: "zh",
  setLang: () => {},
  t: (k) => k,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    try { return (localStorage.getItem("jb_lang") as Lang) || "zh"; } catch { return "zh"; }
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem("jb_lang", l); } catch {}
  };

  const t = (key: TranslationKey, vars?: Record<string, string>): string => {
    let str: string = (translations[lang] as Record<string, string>)[key] ?? (translations.zh as Record<string, string>)[key] ?? key;
    if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v); });
    return str;
  };

  useEffect(() => {
    document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  }, [lang]);

  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>;
}

export function useLang() {
  return useContext(LangContext);
}
